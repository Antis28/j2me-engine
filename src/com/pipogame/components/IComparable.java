 
package com.pipogame.components;

/**
 * Định nghĩa các đối tượng có thể so sánh với nhau được.
 * @author Honghai
 * @company B-Gate
 */
public interface IComparable {
    /**
     * So sánh với đối tượng <code>IComparable</code> khác.
     * @param ic đối tượng cần so sánh.
     * @return &gt; 0 nếu &gt <code>ic</code>.<br/> &lt; 0 nếu ngược lại.
     */
    int compareWith(IComparable ic);
}
